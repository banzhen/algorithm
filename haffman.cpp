#include<stdio.h>
#include<conio.h> 

#define MAXBIT      100
#define MAXVALUE  10000
#define MAXLEAF     30
#define MAXNODE    MAXLEAF*2 -1

typedef struct 
{
    int bit[MAXBIT];  //这个是用来记录hufman的 
    int start;
} HCodeType;        /* 编码结构体 */
typedef struct
{
    int weight;
    int parent;
    int lchild;
    int rchild;
} HNodeType;        

//设置huffman树 
void HuffmanTree (HNodeType HuffNode[MAXNODE],  int n)
{ 
    /* m1、m2：构造哈夫曼树两个最小权值结点的权值，
        x1、x2：构造哈夫曼树两个最小权值结点在数组中的序号。*/
    int i, j, m1, m2, x1, x2;
    /* 初始化 HuffNode[] 中的结点 */ 
    for (i=0; i<2*n-1; i++)
    {
        HuffNode[i].weight = 0;
        HuffNode[i].parent =-1;
        HuffNode[i].lchild =-1;
        HuffNode[i].rchild =-1;
    } 

    /* 输入 n 个叶子结点的权值 */
    for (i=0; i<n; i++)
    {
        printf ("请输入节点的权值： %d: \n", i);
        scanf ("%d", &HuffNode[i].weight);
    } 

    /* 构造 Huffman 树 */
    for (i=0; i<n-1; i++)
    {
        m1=m2=MAXVALUE;     /* m1、m2中存放两个无父结点且结点权值最小的两个结点 */
        x1=x2=0;   //x1,x2用来记录下标 
        /* 找出所有结点中权值最小、无父结点的两个结点，并合并之为一颗二叉树 */
        for (j=0; j<n+i; j++)
        {
        	//同时找到两个最小值 
            if (HuffNode[j].weight < m1 && HuffNode[j].parent==-1)
            {
                m2=m1; 
                x2=x1; 
                m1=HuffNode[j].weight;
                x1=j;
            }
            else if (HuffNode[j].weight < m2 && HuffNode[j].parent==-1)
            {
                m2=HuffNode[j].weight;
                x2=j;
            }
        } 
        
        //从n个开始，才是非叶子节点 
		/* 找两个子结点 x1、x2 的父结点 */
        HuffNode[x1].parent  = n+i;
        HuffNode[x2].parent  = n+i; //设置两个父节点 
        HuffNode[n+i].weight = HuffNode[x1].weight + HuffNode[x2].weight;
        HuffNode[n+i].lchild = x1;
        HuffNode[n+i].rchild = x2;

        printf ("第 %d 层是: %d, %d\n", i+1, HuffNode[x1].weight, HuffNode[x2].weight);  
        printf ("\n");
    } 
} 

int main()
{
	
	
    HNodeType HuffNode[MAXNODE];            /* 定义一个结点结构体数组 */
    HCodeType HuffCode[MAXLEAF],  cd;       /* 定义一个编码结构体数组 */
    int i, j, c, p, n;
    printf ("请输入节点个数:\n");
    scanf ("%d", &n);
    //构造huffman树 
	HuffmanTree (HuffNode, n);
    
    
    
     
    for (i=0; i < n; i++)
    {
        cd.start = n-1;
        c = i;
        p = HuffNode[c].parent;
        while (p != -1)   
        {
            if (HuffNode[p].lchild == c)
                cd.bit[cd.start] = 0;
            else
                cd.bit[cd.start] = 1;
            cd.start--;        /* 求编码的低一位 */
            c=p;                    
            p=HuffNode[c].parent;    
        }  
        
        
        for (j=cd.start+1; j<n; j++)
        { 
			HuffCode[i].bit[j] = cd.bit[j];
		}
        HuffCode[i].start = cd.start;
    } 
    
    /* 输出哈夫曼编码 */
    for (i=0; i<n; i++)
    {
        printf ("%d 的哈夫曼编码是: ", i);
        for (j=HuffCode[i].start+1; j < n; j++)
        {
            printf ("%d", HuffCode[i].bit[j]);
        }
        printf ("\n");
    }
    getch();
    return 0;
}
